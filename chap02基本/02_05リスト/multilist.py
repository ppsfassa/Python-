# 攻撃パターン
brave = ['パイソンのこうげき！', 'パイソンは身を守っている', 'パイソンはにげだした'] 
# モンスターの反応パターン
monster = ['魔物たちがはんげきした', '魔物たちは身構えている'] 
# 2つのリストの要素数を調べて少ない方の数をnに代入する
n = min(len(brave), len(monster))
# 少ない方の要素数に合わせて処理を繰り返す
for i in range(n):
     print(brave[i],
           monster [-i-1],
           sep=' --> '
           )
